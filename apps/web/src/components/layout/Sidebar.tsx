interface SidebarProps {
  open: boolean;
  onClose: () => void;
}

export function Sidebar({ open, onClose }: SidebarProps) {
  const pathname = usePathname();
  const { user } = useAuth();

  const navigation: NavigationItem[] = [
    { name: 'Dashboard', href: '/', icon: HomeIcon },
    { name: 'Campaigns', href: '/campaigns', icon: MegaphoneIcon },
    { name: 'Launch Campaign', href: '/workflow', icon: PlayIcon },
    { name: 'PPC Manager', href: '/ppc', icon: ChartIcon, adminOnly: true },
    { name: 'Agents', href: '/agents', icon: BeakerIcon, adminOnly: true },
    { name: 'Waitlist', href: '/admin/waitlist', icon: UserGroupIcon, adminOnly: true },
    { name: 'Conversions', href: '/conversions', icon: DocumentTextIcon },
    { name: 'Settings', href: '/settings/apps', icon: CogIcon },
    { name: 'Team', href: '/team', icon: UserGroupIcon, adminOnly: true },
  ].map(item => ({
    ...item,
    current: pathname === item.href || (item.href === '/ppc' && pathname.startsWith('/ppc')) || (pathname === '/admin/waitlist' && pathname.startsWith('/admin/waitlist')) || (pathname === '/' && item.href === '/'),
  }));

  const filteredNavigation = navigation.filter(item => 
    !item.adminOnly || (user?.role === 'admin' || user?.role === 'analyst')
  );

  const SidebarContent = () => (
    <>
      {/* Logo */}
      <div className="flex items-center justify-between h-16 px-6">
        <SynterLogo />
        <button
          onClick={onClose}
          className="lg:hidden p-2 rounded-lg text-synter-ink-2 hover:text-synter-ink hover:bg-synter-hover transition-all duration-200"
        >
          <svg className="h-6 w-6" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor">
            <path strokeLinecap="round" strokeLinejoin="round" d="M6 18L18 6M6 6l12 12" />
          </svg>
        </button>
      </div>

      {/* Navigation */}
      <nav className="flex-1 p-6 space-y-2 overflow-y-auto">
        {filteredNavigation.map((item, index) => {
          const Icon = item.icon;
          return (
            <Link
              key={item.name}
              href={item.href}
              className={cn(
                'flex items-center gap-3 px-4 py-3 rounded-lg text-sm font-medium transition-all duration-200',
                item.current
                  ? 'bg-synter-volt/10 text-synter-volt'
                  : 'text-synter-ink-2 hover:text-synter-ink hover:bg-synter-hover hover:translate-x-1'
              )}
              onClick={onClose}
            >
              <Icon className="h-5 w-5 flex-shrink-0" />
              <span className="flex-1">{item.name}</span>
              {item.badge && (
                <span className="synter-badge synter-badge-info">
                  {item.badge}
                </span>
              )}
            </Link>
          );
        })}
      </nav>
    </>
  );

  return (
    <>
      {/* Desktop sidebar */}
      <div className="hidden lg:flex flex-col h-full w-[240px] bg-synter-surface-2 sidebar">
        <SidebarContent />
      </div>

      {/* Mobile sidebar */}
      <div
        className={cn(
          'lg:hidden fixed inset-y-0 left-0 z-50 w-[240px] bg-synter-surface-2 transform transition-transform duration-300 ease-spring',
          open ? 'translate-x-0' : '-translate-x-full'
        )}
      >
        <div className="flex flex-col h-full">
          <SidebarContent />
        </div>
      </div>
    </>
  );
}
